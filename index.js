scuberGreetingForFeet(distanceTravelled) {
  if (distanceTravelled <= 400) {
    return ("This one is on me!")
  }

  else if (distanceTravelled > 2000) {
    return("I will gladly take your thirty bucks.")
  }

  else if (distanceTravelled > 2500) {
    return("No can do.")
  }
}
